                  <h2>Pete Romano, ASC</h2>
                  <p class="item-intro text-muted">DP | Ad Astra, Gemini Man, Once Upon a Time in Hollywood</p>
                  <img class="img-fluid d-block mx-auto" src="/cine2019/img/speakers/romano.png" alt="Speaker">
   							<p class="text-left">Pete Romano discovered still photography while working in the catalog printing industry just out of high school. He bought himself a still camera and spent almost all his free time immersed in taking pictures while taking evening classes in photography at Boston College. He subsequently enlisted in the Navy upon learning that he would be accepted into their photo school program. There he received his first training in motion picture photography, and later he was accepted into the rigorous Navy Dive School. He became a U.S. Navy Underwater Cameraman assigned to Combat Camera Group, deployed out of San Diego, CA.
<br /><br />After completing his Navy service, Pete was determined to find a way to make a living in the underwater cinematography field. He was advised that if he had the skills to work on the mechanics of underwater photo equipment he would be more valuable to some of the underwater documentary makers of the time. He enrolled in machining school, and after graduating, found employment with several established underwater cinematographers, working as an assistant/operator, as well as designing and updating their equipment. In 1979, he received an Emmy award for additional photography on the television series, “Mysteries of the Sea.”
<br /><br />In 1980, while working at ILM as an assistant cameraman, Pete was hired to shoot his first freelance underwater commercial at a local swimming pool The experience, using rented 16 mm equipment, proved an extremely frustrating one – so much so, that he decided to build his own housing around the Cinema Products 16 mm Gizmo. He called his product, HydroFlex.
<br /><br />Hands on experiences as a 1st AC on many underwater productions such as “For Your Eyes Only,” ”Never Say Never Again,” “Jaws 3D” and “Splash” convinced him of the need for a standardized, operator-friendly underwater housing system for 35 mm motion picture cameras.
<br /><br />So in 1985, Pete designed and built his first two HydroFlex 35-3 housings around the Arri 35-3 camera. As an underwater cameraman, Pete incorporated elements that he felt would simplify and enhance the underwater shooting process. It wasn’t long before one of the local camera rental houses wanted to offer the HydroFlex housing as a rental option, and a business was born. Since then Pete has continued to refine the housing systems and has developed additional models for new film and digital cameras, taking into account his own needs as well as feedback from assistants and operators working ‘in the trenches.’
<br /><br />In 1987, Pete received his first motion picture credit as Underwater Director of Photography on “Jaws: The Revenge.” The next year Pete was hired as the miniature unit Underwater Director of Photography on “The Abyss,” the largest scale underwater feature ever attempted. This ambitious film required a lot of new technology, including the development of high-powered, safe underwater set lights. Pete and his former partner, Richard Mula, were given the plum assignment, and in 1990, they shared an Academy of Motion Picture Arts & Sciences Technical Achievement Award for the development of the SeaPar 1200w underwater HMI lamp.
<br /><br />Other professional recognitions include a 1995 Technical Achievement Award from the Society of Operating Cameramen (SOC) for the HydroFlex 35-3 housing system, and a second Technical Achievement Award from the Academy of Motion Picture Arts & Sciences in 2001 for the development of theRemote AquaCam. All of this grew out of Pete’s unwavering passion for the craft of underwater cinematography, and his awareness of the need for tools to help cinematographers like himself enlarge the storytelling possibilities of the wet and underwater environment.
<br /><br />In addition to HydroFlex camera and lighting equipment, Pete has designed and built custom underwater housings and lighting for the IMAX Corp., Iwerks Entertainment, Jacques-Yves Cousteau and Jean-Michelle Cousteau Productions.
<br /><br />Pete has amassed some notable feature film credits as Underwater Director of Photography over the last 20 years. They include: “The Fountain”, “Lady in the Water”, “The Life Aquatic with Steve Zissou“, “Men of Honor“, “The Italian Job“, “Pearl Harbor“, “Insomnia“, “Amistad“, “Saving Private Ryan”, “Woman on Top”, “In Dreams“, “The Rock”, “Waterworld“, “Free Willy 1, 2 & 3“, “Flipper“, “Tomorrow Never Dies“, “The New World”, “Max Payne”, “The Tree of Life”, “Farewell Atlantis” and “The Abyss”. His resume also includes numerous television and commercial credits.
<br /><br />In 2002, the Association of Independent Commercial Producers (AICP) honored Pete with an “Excellence in Cinematography” award (shared award), as Underwater Director of Photography on the Pocari sport drink “Tennis” spot. This commercial is now part of the permanent collection in the Dept. of Film & Media, Museum of Modern Art, New York.
<br /><br />Pete is a member of the Academy of Motion Picture Arts & Sciences (AMPAS), the American Society of Cinematographers (ASC), the Directors Guild of America (DGA) and a Director of Photography in the International Cameramans Guild, Local 600 (ICG).
